 function [Param] =  initializeParam(d2D,fc)

%32,66,301 at 28Ghz,  27,55,293 at 60 Ghz and 26,53,291 at >=70Ghz 
%DsNormal = 66e-9;  %median LOS
%DsLong = 301e-9;  % 90 percentile Nlos

cLight = 3e8;
ueVel = 15/18;
hUT = 1.5;
hBS = 10;
DsShort = 32e-9;
ulgZSD = max(-0.21, -14.8*(d2D/1000) + 0.01*abs(hUT-hBS) + 0.83); 
uZSDOffset = 0; 

Param = struct(...
    'fc', 28e9,...
    'cLight', 3e8,...
    'ueVel', 0,...
    'maxDopShift',ueVel*fc/cLight,...
    'hUT', 1.5,...
    'hBS', 10,...
    'DsShort', 32e-9,...
    'DsNormal', 66e-9,...
    'DsLong', 301e-9,...
    'uDs', -0.24*log10 (1+ fc) - 7.14,...
    'uASD', -0.05*log10(1+ fc) + 1.21,...
    'uASA', -0.08*log10(1+ fc) + 1.73,...
    'uZSA', -0.1*log10(1+ fc) + 0.73,...
    'ulgZSD', max(-0.21, -14.8*(d2D/1000) + 0.01*abs(hUT-hBS) + 0.83),...
    'uZSDOffset',0,...
    'uZSD', ulgZSD + uZSDOffset,...
    'cASD', 3,...
    'cASA', 17,...
    'cZSD', (3/8)*10^(ulgZSD),...
    'cZSA', 7,...
    'DsDesired', DsShort,...
    'Kfactor', 9);

end 

