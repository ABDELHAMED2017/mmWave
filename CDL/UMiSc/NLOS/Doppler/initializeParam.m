function [Param] =  initializeParam(d2D,fc)

%32,66,301 at 28Ghz,  27,55,293 at 60 Ghz and 26,53,291 at >=70Ghz 
%DsShort = 32e-9;  %median LOS
%DsLong = 301e-9;  % 90 percentile Nlos

cLight = 3e8;
ueVel = 15/18;
hUT = 1.5;
hBS = 10;
DsNormal = 66e-9;
ulgZSD = max(-0.5, -3.1*(d2D/1000) + 0.01*max(hUT-hBS,0) +0.2);
uZSDOffset = -10^(-1.5*log10(max(10, d2D))+3.3); 

Param = struct(...
    'fc', 28e9,...
    'cLight', 3e8,...
    'ueVel', 0,...
    'maxDopShift',ueVel*fc/cLight,...
    'hUT', 1.5,...
    'hBS', 10,...
    'DsShort', 32e-9,...
    'DsNormal', 66e-9,...
    'DsLong', 301e-9,...
    'uDs', -0.24*log10(1+ fc) - 6.83,...
    'uASD', -0.23*log10(1+ fc) + 1.53,...
    'uASA', -0.08*log10(1+ fc) + 1.81,...
    'uZSA', -0.04*log10(1+ fc) + 0.92,...
    'ulgZSD', max(-0.5, -3.1*(d2D/1000) + 0.01*max(hUT-hBS,0) +0.2),...
    'uZSDOffset',-10^(-1.5*log10(max(10, d2D))+3.3),...
    'uZSD', ulgZSD + uZSDOffset,...
    'cASD', 10,...
    'cASA', 22,...
    'cZSD', (3/8)*10^(ulgZSD),...
    'cZSA', 7,...
    'DsDesired', DsNormal);

end 

