function [Param] =  initializeParam(d2D,fc)

%32,66,301 at 28Ghz,  27,55,293 at 60 Ghz and 26,53,291 at >=70Ghz 
%DsShort = 32e-9;  %median LOS
%DsLong = 301e-9;  % 90 percentile Nlos

cLight = 3e8;
ueVel = 15/18;
hUT = 1.5;
hBS = 25;
DsNormal = 66e-9;
ulgZSD = max(-0.5, -2.1*(d2D/1000) - 0.01*(hUT - 1.5)+0.9);
uZSDOffset = 7.66*log10(fc)-5.96 - 10^((0.208*log10(fc)- 0.782)*log10(max(25,d2D - 0.13*log10(fc)+2.03)));

Param = struct(...
    'fc', 28e9,...
    'cLight', 3e8,...
    'ueVel', 0,...
    'maxDopShift',ueVel*fc/cLight,...
    'hUT', 1.5,...
    'hBS', 10,...
    'DsShort', 32e-9,...
    'DsNormal', 66e-9,...
    'DsLong', 301e-9,...
    'uDs', -6.28 - 0.204*log10(fc),...
    'uASD', 1.5 - 0.1144*log10(fc),...
    'uASA', 2.08 - 0.27*log10(fc),...
    'uZSA', -0.3236*log10(fc) + 1.512,...
    'ulgZSD', max(-0.5, -2.1*(d2D/1000) - 0.01*(hUT - 1.5)+0.9),...
    'uZSDOffset',7.66*log10(fc)-5.96 - 10^((0.208*log10(fc)- 0.782)*log10(max(25,d2D - 0.13*log10(fc)+2.03))),...
    'uZSD', ulgZSD + uZSDOffset,...
    'cASD', 2,...
    'cASA', 15,...
    'cZSD', (3/8)*10^(ulgZSD),...
    'cZSA', 7,...
    'DsDesired', DsNormal);

end 

