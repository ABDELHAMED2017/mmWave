 function [Param] =  initializeParam(d2D,fc)

%32,66,301 at 28Ghz,  27,55,293 at 60 Ghz and 26,53,291 at >=70Ghz 
%DsNormal = 66e-9;  %median LOS
%DsLong = 301e-9;  % 90 percentile Nlos

cLight = 3e8;
ueVel = 15/18;
hUT = 1.5;
hBS = 25; %#ok<*NASGU>
DsShort = 32e-9;
ulgZSD = max(-0.5, -2.1*(d2D/1000) - 0.01*(hUT - 1.5)+0.75); 
uZSDOffset = 0;
 

Param = struct(...
    'fc', 28e9,...
    'cLight', 3e8,...
    'ueVel', 0,...
    'maxDopShift',ueVel*fc/cLight,...
    'hUT', 1.5,...
    'hBS', 10,...
    'DsShort', 32e-9,...
    'DsNormal', 66e-9,...
    'DsLong', 301e-9,...
    'uDs', -6.955 - 0.0963*log10(fc),...
    'uASD', -0.05*log10(1+ fc) + 1.21,...
    'uASA', 1.81,...
    'uZSA', 0.95,...
    'ulgZSD', max(-0.5, -2.1*(d2D/1000) - 0.01*(hUT - 1.5)+0.75),...
    'uZSDOffset', 0,...
    'uZSD', ulgZSD + uZSDOffset,...
    'cASD', 5,...
    'cASA', 11,...
    'cZSD', (3/8)*10^(ulgZSD),...
    'cZSA', 7,...
    'DsDesired', DsShort,...
    'Kfactor', 9);

end 

